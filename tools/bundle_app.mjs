import { mkdir, readFile, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const ROOT = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const ENTRY = path.join(ROOT, "app.js");
const OUTPUT = path.join(ROOT, "dist", "app.bundle.js");

const importPattern = /^\s*import\s*\{([\s\S]*?)\}\s*from\s*["'](.+?)["'];\s*/gm;
const moduleIds = new Map();
const modules = [];

function slash(filePath) {
  return path.relative(ROOT, filePath).replaceAll(path.sep, "/");
}

function resolveImport(fromFile, source) {
  if (!source.startsWith(".")) {
    throw new Error(`Only relative imports are supported: ${source}`);
  }
  return path.resolve(path.dirname(fromFile), source);
}

function parseImportSpecifiers(specifiers) {
  return specifiers
    .split(",")
    .map((specifier) => specifier.trim())
    .filter(Boolean)
    .map((specifier) => {
      const aliasMatch = specifier.match(/^([A-Za-z_$][\w$]*)\s+as\s+([A-Za-z_$][\w$]*)$/);
      if (aliasMatch) {
        return { imported: aliasMatch[1], local: aliasMatch[2] };
      }
      return { imported: specifier, local: specifier };
    });
}

function destructureImports(imports) {
  return imports
    .map(({ moduleId, specifiers }) => {
      const bindings = specifiers
        .map(({ imported, local }) => (imported === local ? imported : `${imported}: ${local}`))
        .join(", ");
      return `const { ${bindings} } = ${moduleId};`;
    })
    .join("\n");
}

function parseImports(source, filePath) {
  const imports = [];
  const strippedSource = source.replace(importPattern, (_match, specifiers, importSource) => {
    imports.push({
      source: resolveImport(filePath, importSource),
      specifiers: parseImportSpecifiers(specifiers),
    });
    return "";
  });
  return { imports, strippedSource };
}

function transformExports(source) {
  const exports = new Map();
  let transformed = source;

  transformed = transformed.replace(
    /^export\s+(async\s+function|function|class)\s+([A-Za-z_$][\w$]*)/gm,
    (_match, declarationType, name) => {
      exports.set(name, name);
      return `${declarationType} ${name}`;
    },
  );
  transformed = transformed.replace(
    /^export\s+(const|let|var)\s+([A-Za-z_$][\w$]*)/gm,
    (_match, declarationType, name) => {
      exports.set(name, name);
      return `${declarationType} ${name}`;
    },
  );
  transformed = transformed.replace(/^export\s*\{([\s\S]*?)\};?\s*/gm, (_match, specifiers) => {
    for (const specifier of parseImportSpecifiers(specifiers)) {
      exports.set(specifier.local, specifier.imported);
    }
    return "";
  });

  return {
    source: transformed.trim(),
    exports: [...exports.entries()].map(([exported, local]) => ({ exported, local })),
  };
}

async function readModule(filePath) {
  const normalized = path.resolve(filePath);
  if (moduleIds.has(normalized)) {
    return moduleIds.get(normalized);
  }

  const moduleId = `__module${moduleIds.size}`;
  moduleIds.set(normalized, moduleId);

  const source = await readFile(normalized, "utf8");
  const { imports, strippedSource } = parseImports(source, normalized);
  const dependencyBindings = [];
  for (const moduleImport of imports) {
    dependencyBindings.push({
      moduleId: await readModule(moduleImport.source),
      specifiers: moduleImport.specifiers,
    });
  }

  const transformed = transformExports(strippedSource);
  modules.push({
    id: moduleId,
    filePath: normalized,
    importBindings: destructureImports(dependencyBindings),
    source: transformed.source,
    exports: transformed.exports,
  });
  return moduleId;
}

function renderModule(module) {
  const returnedExports = module.exports
    .map(({ exported, local }) => (exported === local ? exported : `${exported}: ${local}`))
    .join(", ");
  const body = [module.importBindings, module.source].filter(Boolean).join("\n\n");
  return [
    `// ${slash(module.filePath)}`,
    `const ${module.id} = (() => {`,
    indent(body),
    `return { ${returnedExports} };`,
    `})();`,
  ].join("\n");
}

function indent(source) {
  return source
    .split("\n")
    .map((line) => (line ? `  ${line}` : ""))
    .join("\n");
}

async function bundle() {
  const entrySource = await readFile(ENTRY, "utf8");
  const { imports, strippedSource } = parseImports(entrySource, ENTRY);
  const entryBindings = [];
  for (const moduleImport of imports) {
    entryBindings.push({
      moduleId: await readModule(moduleImport.source),
      specifiers: moduleImport.specifiers,
    });
  }

  const bundleSource = [
    "// Generated by tools/bundle_app.mjs. Edit source modules, not this file.",
    '"use strict";',
    "(() => {",
    indent(modules.map(renderModule).join("\n\n")),
    "",
    indent("// app.js"),
    indent(destructureImports(entryBindings)),
    "",
    indent(strippedSource.trim()),
    "})();",
    "",
  ].join("\n");

  await mkdir(path.dirname(OUTPUT), { recursive: true });
  await writeFile(OUTPUT, bundleSource, "utf8");
  console.log(`Wrote ${slash(OUTPUT)} (${modules.length} modules)`);
}

bundle().catch((error) => {
  console.error(error);
  process.exitCode = 1;
});
